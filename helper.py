def activity_heatmap(selected_user,df):

    if selected_user != 'Overall':
        df = df[df['user'] == selected_user]

    user_heatmap = df.pivot_table(index='day_name', columns='period', values='message', aggfunc='count').fillna(0)

    return user_heatmap

def plot_word_treemap(df, selected_user):
    if selected_user != 'Overall':
        df = df[df['user'] == selected_user]

    text = ' '.join(df['message'])
    words = text.lower().split()
    common = Counter(words).most_common(50)
    df_freq = pd.DataFrame(common, columns=['word', 'count'])

    fig = px.treemap(df_freq, path=['word'], values='count',
                     title='Top 50 Words Treemap', color='count',
                     color_continuous_scale='Blues')
    st.plotly_chart(fig)